#list,tuple,set,dict
#Creation of list
lst1=[10,20,30,40]#Storing the data with the same data type(Homogeneous type ofdata)
print(lst1,type(lst1));

lst2=[100,134.87,True,6+8j,"kodnest",'k']#Heterogeneous type of data
print(lst2,type(lst2));


#Creation of tuple:
tup1=(10,20,30,40)
print(tup1,type(tup1))

tup2=(100,134.87,True,6+8j,"kodnest",'k')
print(tup2,type(tup2))


#Creation of Set:
s1={10,20,30,40}
print(s1,type(s1))

s2={100,134.87,True,6+8j,"kodnest",'k'}
print(s2,type(s2))


#Creation of dict{key:value}
d1={'name':'ram','age':30,'city':'benglouru'}
print(d1,type(d1))

d2={1:'one',2:'two',3:'three'}
print(d2,type(d2))
